%区间[-1,3],最终区间长度为原始区间长度的0.08
[best_x best_fx]=golddiv(y,x,-1,3,0.32);  %使用黄金分割法求解 y为函数，x为函数y的变量，a,b为下单峰区间[a,b],epsilon为精确度
